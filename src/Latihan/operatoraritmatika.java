
package Latihan;
//PEMBUKAAAN
public class operatoraritmatika {
    public static void main(String[] args) {
    int A = 10;
    int B = 20;
    int C; 
    //PENJUMLAHAN
    C = A+B;
    System.out.println(A +"+"+B+"=" +C);
    //PENGURAGAN
    C = A-B;
    System.out.println(A +"-"+B+"=" +C);
    //PEMBAGIAN
    C = A/B;
    System.out.println(A+":"+B+"=" +C);
    //Sisa Hasil Bagi
    C = A%B;
    System.out.println(A+ "SISA BAGINYA DENGAN" +B+ "ADALAH  " +C);
    
}
}
